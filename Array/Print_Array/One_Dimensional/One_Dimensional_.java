package Array.Print_Array.One_Dimensional;

import java.util.Arrays;

public class One_Dimensional_ {
    public static void main(String[] args) {

        // Printing	a 1D Array          -- Arrays.toString(arr)

        int[] arr = {25, 30, 50, 10, 5};

        System.out.println("Print 1D array using reference : " + arr);    //[I@6db3f829

        System.out.println("Print 1D array using Arrays.toString(arr) method:  " +

                Arrays.toString(arr)); //[25,	30,	50,	10,	5]
    }
}
