package Java.Big_O_Notation.Complexity_Evaluation.Time_Complexity.Indivisible_Language_Operation;

/*                                    Indivisible Language Operations

    For boolean, char, short, int, double, float data types the following operations are considered
        indivisible operation executed in 1 operation:

        1.Arithmetic and logical operations:
            +,-,*,/,++,!, |, ||, &, &&

        2. Comparison operations
            ==,!=,>,<, >=,<=

        3.  Variable initialization:
            int i = 5, char c ='k', double d = 1.3;

        4. Return from a function
            return

        5. Print function is considered to be executed in 1 operation

*/