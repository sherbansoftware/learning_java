package Java.Algorithms_in_Java;

/*
                                                    Java Sorting Algorithms

    Quick Sort
        Quicksort is a divide and conquer algorithm, which means original array is divided into two arrays, each of them
        is sorted individually and then sorted output is merged to produce the sorted array.

    Merge Sort
        Merge sort is used when the data structure doesn't support random access, since it works with pure sequential access
        (forward iterators, rather than random access iterators). It’s also widely used for external sorting, where random
        access can be very, very expensive compared to sequential access.

    Bubble Sort
        Bubble Sort is a simple and slow sorting algorithm that repeatedly steps through the collection, compares each
        pair of adjacent elements and swaps them if they are in the wrong order.

    Selection Sort
        Selection Sort is a simple and slow sorting algorithm that repeatedly selects the lowest or highest element from
        the un-sorted section and moves it to the end of the sorted section.

    Insertion Sort
        Insertion Sort is a simple and slow sorting algorithm that repeatedly takes the next element from the un-sorted
        section and inserts it into the sorted section at the correct position.

*/