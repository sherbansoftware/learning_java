/*                                               Priority Queue

    is a special type of queue in which each element is associated with a priority and is served according to its priority.
    If elements with the same priority occur, they are served according to their order in the queue.

    Generally, the value of the element itself is considered for assigning the priority.

    For example, The element with the highest value is considered as the highest priority element. However, in other cases,
    we can assume the element with the lowest value as the highest priority element.
    In other cases, we can set priorities according to our needs.


    Difference between Priority Queue and Normal Queue:

        • In a queue, the first-in-first-out (FIFO) rule is implemented
        • In a priority queue, the values are removed on the basis of priority.

        The element with the highest priority is removed first.

    Implementation of Priority Queue:

        Priority queue can be implemented using an:

        • array
        • linked list
        • heap data structure     -efficient implementation
        • a binary search tree

    Among these data structures,
        heap data structure provides an efficient implementation of priority queues.



    Some of the applications of a Priority Queue are:

        • Dijkstra's algorithm
        • For implementing stack
        • For load balancing and interrupt handling in an operating system
        • For data compression in Huffman code


*/