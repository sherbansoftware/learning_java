package Functional_Programming.Streams.Operations.Terminal_Operations.Reduction.Reduce.IntSummaryStatistics_.Example;

public class Employee {
    private String name;
    private String city;
    private int age;
    private double salary;
    public Employee(String name, String city, int age, double salary) {
        this.name = name;
        this.city = city;
        this.age = age;
        this.salary = salary;
    }
    public String getName() {
        return name;
    }
    public String getCity() {
        return city;
    }
    public int getAge() {
        return age;
    }
    public double getSalary() {
        return salary;
    }
}
