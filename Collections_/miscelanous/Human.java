package Collections_.miscelanous;

public class Human {

    private String name;
    private int years;

    public Human(String name, int years) {
        this.name = name;
        this.years = years;
    }

    public String getName() {
        return name;
    }

    public int getYears() {
        return years;
    }

    public void setName(String name) {
        this.name = name;
    }
}
