package Variables.Variable.Kinds_Of_Variable;

/*                                                         A constant

    is a variable whose value cannot change once it has been assigned. Java doesn't have built-in support for constants.
    A constant can make our program more easily read and understood by others. In addition, a constant is cached by the
        JVM as well as our application, so using a constant can improve performance.
   To define a variable as a constant, we just need to add the keyword “final” in front of the variable declaration.

 */


public class Constant_Variable {
    //constant fields
    static final int NUM = 6;
}
