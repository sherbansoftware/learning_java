package Number_System.Octal_Number_System;

//


/*                                            Octal Number System


    ○ The octal numeral system, or oct for short, is the base-8 number system, and uses the digits 0 to 7,


                                                Octal literals


    ○ Literals with a leading zero are octal literals. Any number prefixed with a 0 is considered octal.
      Octal numbers can only use

                  * digits 0-7,

                  * just like decimal can use 0-9,

                  * and binary can use 0-1.

          To define integer literals as octal value in Java is effortless.


 */

public class Octal_Number_System {
    public static void main(String[] args) {
        int octal = 0145671; // any number prefixed with a 0 is considered octal.
        System.out.println(octal);
    }

}
