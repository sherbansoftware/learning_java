package Blocks_and_Statements;

/*
                                                   Java Statements

    https://docs.oracle.com/javase/specs/jls/se8/html/jls-14.html#jls-14.5


                    Java statements are instructions that tell the programming language what to do.
                    ATest.A basic statement, like an assignment statement, assigns a value to a variable

       Example:

          declaration statement: --------------------------------------------------------------------------------------

                    int number;

           expression statement: ---------------------------------------------------------------------------------------

                     number = 4;

          complete declaration and expression statement:

                     double entryFee = 15.75;
                     boolean isFinished = false;
                     String welcomeMessage = "Hello!";

          control flow statement: --------------------------------------------------------------------------------------

           if (number < 10 )
                {
                    //expression statement
                    System.out.println(number + " is less than ten");
                 }

                 All Java statements must end in a semicolon (;). This tells Java that it should attempt to process
                 all information up to that semicolon.

* */